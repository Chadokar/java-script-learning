// rules for nameing variables 

// you cannot start with number 
// example :-
// 1value (invalid)
// value1 (valid)

var value1 = 10;
console.log(value1/2);

// you can use only underscore _ or dollar symbol 
// first_name (valid)
// _firstname (valid)

// first$name (valid)
// $firstname (valid)

// you cannot use spaces
var first_name = "shubham"; //snake case writing
var firstName = "shubham"; //camel case writing
// first name (invalid)

